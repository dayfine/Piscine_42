int		ft_sqrt(int nb)
{
	int trial;

	trial = 1;
	if (nb <= 0)
	{
		return (0);
	}
	while (trial * trial <= nb)
	{
		if (trial * trial == nb)
		{
			return (trial);
		}
		trial++;
	}
	return (0);
}
