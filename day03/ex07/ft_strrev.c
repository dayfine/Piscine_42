char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	int		len;
	char	temp;

	while (str[len] != '\0')
	{
		len++;
	}
	i = 0;
	j = len - 1;
	while (i < j)
	{
		temp = *(str + i);
		*(str + i) = *(str + j);
		*(str + j) = temp;
		i++;
		j--;
	}
	return (str);
}
